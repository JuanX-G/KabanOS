#include "../serial/serial.h"
#include "pic.h"

void pic_remap() {
	outb(0x20, 0x11);
	outb(0xA0, 0x11);
	outb(0x21, 0x20);
	outb(0xA1, 0x28);
	outb(0x21, 0x04);
	outb(0xA1, 0x02);
	outb(0x21, 0x01);
	outb(0xA1, 0x01);
	outb(0x21, 0);
	outb(0xA1, 0);
}

void pit_init() {
	outb(0x43, 0x36);
	uint16_t divisor = 1193180 / 100;
	outb(0x40, divisor & 0xFF);
	outb(0x40, divisor >> 8);
}

